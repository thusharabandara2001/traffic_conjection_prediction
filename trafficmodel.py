# 7. Saving Model, Scalers, and Feature Names
deployment_artifacts = {
    'scaler_X': scaler_X,
    'feature_names': X.columns.tolist(),
    'traffic_threshold': TRAFFIC_THRESHOLD
}
joblib.dump(deployment_artifacts, 'scalers_and_features_classification.pkl')
model.save('traffic_classification_model.keras')
print("--- Artifacts Saved Successfully (Classification Model) ---")

# 8. User Input and Prediction Function

def get_user_input_prediction():
    """
    Loads saved artifacts, prompts the user for necessary features,
    and makes a traffic class prediction (0 or 1).
    """
    try:
        # Load artifacts
        loaded_artifacts = joblib.load('scalers_and_features_classification.pkl')
        loaded_model = load_model('traffic_classification_model.keras')
        scaler_X = loaded_artifacts['scaler_X']
        feature_names = loaded_artifacts['feature_names']
        traffic_threshold = loaded_artifacts['traffic_threshold']

        print("\n--- Traffic Classification from User Input ---")
        print(f"Prediction will be 1 (Traffic) if volume is predicted above {traffic_threshold}.")

        # Define the primary features that need explicit user input
        primary_features = [
            'is_holiday', 'hour', 'weekday', 'month', 'year',
            'temperature', 'air_pollution_index', 'humidity', 'wind_speed', 'wind_direction',
            'visibility_in_miles', 'dew_point', 'rain_p_h', 'snow_p_h', 'clouds_all',
            'last_1_hour_traffic', 'last_2_hour_traffic', 'last_3_hour_traffic',
            'last_4_hour_traffic', 'last_5_hour_traffic', 'last_6_hour_traffic',
            'weather_description'
        ]

        input_data = {}
        print("\nPlease enter the following details:")

        # Collect numerical and categorical inputs
        for feature in primary_features:
            if feature == 'weather_description':
                print("\nExample weather descriptions: light rain, clear, mist, overcast clouds, etc.")
                weather_input = input(f"Enter the '{feature}' (e.g., clear, light rain): ").strip().lower()
                input_data[feature] = weather_input
            elif feature == 'is_holiday':
                val = input(f"Enter '{feature}' (0 or 1 for no/yes): ")
                input_data[feature] = int(val)
            else:
                try:
                    val = float(input(f"Enter value for '{feature}': "))
                    input_data[feature] = val
                except ValueError:
                    print(f"Invalid input for {feature}. Please enter a number.")
                    return

        # Data Preprocessing for Prediction


        input_df = pd.DataFrame([input_data])

        weather_desc = input_df['weather_description']
        input_df.drop('weather_description', axis=1, inplace=True)

        prediction_row = pd.DataFrame(0, index=[0], columns=feature_names)

        for col in input_df.columns:
            if col in prediction_row.columns:
                prediction_row.loc[0, col] = input_df.loc[0, col]

        weather_col_name = f'weather_description_{weather_desc.iloc[0]}'

        if weather_col_name in prediction_row.columns:
            prediction_row.loc[0, weather_col_name] = 1
        else:
            print(f"\nWarning: '{weather_desc.iloc[0]}' is an unseen weather description. Treating it as 0.")

        X_pred_scaled = scaler_X.transform(prediction_row)
        Y_pred_proba = loaded_model.predict(X_pred_scaled)
        Y_pred_class = (Y_pred_proba[0][0] > 0.5).astype(int)

        result_text = "🚨 TRAFFIC JAM " if Y_pred_class == 1 else "🟢 NO JAM EXPECTED "

        print("\n" + "="*60)
        print(f"Predicted Traffic Class: {result_text}")
        print(f"Model Probability for Traffic (1): {Y_pred_proba[0][0]:.4f}")
        print(f"(Based on traffic volume > {traffic_threshold} vehicles)")
        print("="*60 + "\n")

    except FileNotFoundError:
        print("\nError: Classification model artifacts not found.")
        print("Please ensure the training and saving sections run successfully first.")
    except Exception as e:
        print(f"\nAn error occurred during prediction: {e}")

# 9. Call the prediction function
if __name__ == '__main__':
    get_user_input_prediction()
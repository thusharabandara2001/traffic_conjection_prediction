import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from tensorflow.keras.models import Sequential, load_model
from tensorflow.keras.layers import Dense
from tensorflow.keras.optimizers import Adam
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score, confusion_matrix
import joblib
import matplotlib.pyplot as plt

# 1. Data Loading and Cleaning 
file_name = "traffic_volume_data.csv"
df = pd.read_csv(file_name)

df.dropna(subset=['traffic_volume', 'last_1_hour_traffic'], inplace=True)
df['weather_description'] = df['weather_description'].str.lower()

# use a threshold of 2000 vehicles to define "traffic"
TRAFFIC_THRESHOLD = 2000
df['is_traffic'] = (df['traffic_volume'] > TRAFFIC_THRESHOLD).astype(int)
print(f"Traffic Threshold set to {TRAFFIC_THRESHOLD} vehicles.")
print(f"Target distribution (0: No Traffic, 1: Traffic):")
print(df['is_traffic'].value_counts())

# 2. Feature Selection and Engineering 
target = 'is_traffic'
selected_features = [
    'is_holiday', 'hour', 'weekday', 'month', 'year',
    'temperature', 'air_pollution_index', 'humidity', 'wind_speed', 'wind_direction',
    'visibility_in_miles', 'dew_point', 'rain_p_h', 'snow_p_h', 'clouds_all',
    'last_1_hour_traffic', 'last_2_hour_traffic', 'last_3_hour_traffic',
    'last_4_hour_traffic', 'last_5_hour_traffic', 'last_6_hour_traffic',
    'weather_description'
]
data = df[selected_features + [target]].copy()
data = pd.get_dummies(data, columns=['weather_description'], drop_first=True)
X = data.drop(target, axis=1)
Y = data[target]
X.ffill(inplace=True)
X.dropna(inplace=True)
Y = Y[X.index]

# 3. Scaling and Splitting (scaler_Y is no longer needed)
X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.2, random_state=42)
scaler_X = StandardScaler()
X_train_scaled = scaler_X.fit_transform(X_train)
X_test_scaled = scaler_X.transform(X_test)

# 4. ANN Model Creation and Training 
input_dim = X_train_scaled.shape[1]
model = Sequential([
    Dense(128, activation='relu', input_shape=(input_dim,)),
    Dense(64, activation='relu'),
    Dense(32, activation='relu'),
    Dense(1, activation='sigmoid')
])
model.compile(optimizer=Adam(learning_rate=0.001), loss='binary_crossentropy', metrics=['accuracy'])
history = model.fit(
    X_train_scaled, Y_train,
    epochs=10,
    batch_size=32,
    validation_split=0.1,
    verbose=0
)

# 5. Evaluation and Classification Metrics Calculation
Y_pred_proba = model.predict(X_test_scaled)
Y_pred_class = (Y_pred_proba > 0.5).astype(int)

# Calculate Classification Metrics
accuracy = accuracy_score(Y_test, Y_pred_class)
precision = precision_score(Y_test, Y_pred_class)
recall = recall_score(Y_test, Y_pred_class)
f1 = f1_score(Y_test, Y_pred_class)
cm = confusion_matrix(Y_test, Y_pred_class)

print("\n--- Model Evaluation (Classification) on Test Set ---")
print(f"Accuracy: {accuracy:.4f}")
print(f"Precision: {precision:.4f}")
print(f"Recall: {recall:.4f}")
print(f"F1 Score: {f1:.4f}")
print("\nConfusion Matrix:")
print(cm)

# 6. Plot 1: Model Loss
plt.figure(figsize=(12, 5))
plt.plot(history.history['loss'], label='Train Loss')
plt.plot(history.history['val_loss'], label='Validation Loss')
plt.title('Model Loss (Binary Crossentropy) Over Epochs')
plt.ylabel('Loss')
plt.xlabel('Epoch')
plt.legend(loc='upper right')
